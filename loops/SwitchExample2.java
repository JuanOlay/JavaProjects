public class SwitchExample2 {  
public static void main(String[] args) {  
    int numero=20;  

    switch(numero){  

    case 10: System.out.println("10");  
    case 20: System.out.println("20");  
    case 30: System.out.println("30");  
    default:System.out.println("No es ni 10 ni 20 ni 30");  
    }  
}  
}  