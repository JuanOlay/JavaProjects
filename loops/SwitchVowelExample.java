public class SwitchVowelExample {    
public static void main(String[] args) {    
    char ch='O';    
    switch(ch)  
    {  
        case 'a':   
            System.out.println("vocal");  
            break;  
        case 'e':   
            System.out.println("vocal");  
            break;  
        case 'i':   
            System.out.println("vocal");  
            break;  
        case 'o':   
            System.out.println("vocal");  
            break;  
        case 'u':   
            System.out.println("vocal");  
            break;  
        case 'A':   
            System.out.println("vocal");  
            break;  
        case 'E':   
            System.out.println("vocal");  
            break;  
        case 'I':   
            System.out.println("vocal");  
            break;  
        case 'O':   
            System.out.println("vocal");  
            break;  
        case 'U':   
            System.out.println("vocal");  
            break;  
        default:   
            System.out.println("Consonante");  
    }  
}    
}   