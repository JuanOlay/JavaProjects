public class CompoundOperatorsExample{
	public static void main(String[] args){

	int numero = 10;
	System.out.println(numero);
	System.out.println(numero += 1);
	System.out.println(numero -= 1);
	System.out.println(numero *= 1);
	System.out.println(numero /= 1);
	System.out.println(numero %= 1);
	}
}
